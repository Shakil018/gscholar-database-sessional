CREATE OR REPLACE FUNCTION CHECK_CONTAINS(TEMP_DOC_ID IN NUMBER, TEMP_WHERE IN VARCHAR2, TEMP_STRING IN VARCHAR2)
RETURN VARCHAR2 IS
	TEMP_TITLE VARCHAR2(1000);
	TEMP_DESCRIPTIONS CLOB;
BEGIN

	SELECT TITLE, DESCRIPTIONS INTO TEMP_TITLE, TEMP_DESCRIPTIONS FROM DOCUMENT WHERE DOC_ID = TEMP_DOC_ID;

	IF LOWER(TEMP_WHERE) LIKE LOWER('title') THEN
		IF INSTR( LOWER(TEMP_TITLE), LOWER(TEMP_STRING) ) > 0 THEN
			RETURN 'true';
		ELSE 
			RETURN 'false';
		END IF;
	
	ELSE 
		IF INSTR( LOWER(TEMP_DESCRIPTIONS), LOWER(TEMP_STRING) ) > 0 OR INSTR( LOWER(TEMP_TITLE), LOWER(TEMP_STRING) ) > 0 THEN
			RETURN 'true';
		ELSE 
			RETURN 'false';
		END IF;
	END IF;

END;
