CREATE OR REPLACE FUNCTION CHECK_BETWEEN(TEMP_DOC_ID IN NUMBER, TEMP_DATE_1 IN VARCHAR2, TEMP_DATE_2 IN VARCHAR2)
RETURN VARCHAR2 IS
	TEMP_UPLOAD_TIME DATE;
	DATE_1 DATE;
	DATE_2 DATE;
BEGIN
	
	DATE_1 := TO_DATE(TEMP_DATE_1, 'DD-MON-YYYY');
	DATE_2 := TO_DATE(TEMP_DATE_2, 'DD-MON-YYYY');
	
	
	SELECT UPLOAD_TIME INTO TEMP_UPLOAD_TIME FROM DOCUMENT WHERE DOC_ID = TEMP_DOC_ID;
	
	IF TEMP_UPLOAD_TIME >= DATE_1 AND TEMP_UPLOAD_TIME <= DATE_2 THEN
		RETURN 'true';
	ELSE 
		RETURN 'false';
	END IF;
	
END;
/

