CREATE OR REPLACE PROCEDURE AFF_HANDLE(ID_PERSON IN NUMBER, INST IN VARCHAR2, DESIG IN VARCHAR2) IS
	INST_COUNT NUMBER;
	AFF_ID NUMBER;
BEGIN
	SELECT COUNT(*) INTO INST_COUNT
	FROM AFFILIATION 
	WHERE LOWER(NAME) LIKE LOWER(INST);
	
	DBMS_OUTPUT.PUT_LINE('GOT THE COUNT : ' || INST_COUNT);
	
	IF INST_COUNT = 0 THEN
		INSERT INTO AFFILIATION (AFFILIATION_ID, NAME) VALUES(AFFILIATION_ID_GENERATOR.NEXTVAL, INST);
		COMMIT;
		AFF_ID := AFFILIATION_ID_GENERATOR.CURRVAL;
	ELSE 
		SELECT AFFILIATION_ID INTO AFF_ID FROM AFFILIATION WHERE LOWER(NAME) LIKE LOWER(INST);
	END IF;
		
	INSERT INTO AFF_WITH VALUES(ID_PERSON, AFF_ID, DESIG);
	COMMIT;
END;
